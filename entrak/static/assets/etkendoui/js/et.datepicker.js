        _update: function(value) {
            var that = this,
                options = that.options,
                min = options.min,
                max = options.max,
                current = that._value,
                date = parse(value, options.parseFormats, options.culture),
                isSameType = (date === null && current === null) || (date instanceof Date && current instanceof Date),
                formattedValue;

            /* Force update again because the select option might be changed
            if (+date === +current && isSameType) {
                formattedValue = kendo.toString(date, options.format, options.culture);

                if (formattedValue !== value) {
                    that.element.val(date === null ? value : formattedValue);
                }

                return date;
            }
            */

            if (date !== null && isEqualDatePart(date, min)) {
                date = restrictValue(date, min, max);
            } else if (!isInRange(date, min, max)) {
                date = null;
            }

            that._value = date;
            that.dateView.value(date);

            var date_text = date ? kendo.toString(date, options.format, options.culture) : value;
            if (date && options.select == 'week') {
                function getSunday(d) {
                    var sunday = new Date(d);
                    var day = d.getDay(),
                        diff = d.getDate() - day;

                    sunday.setDate(diff);
                    return sunday;
                }

                var start_date = getSunday(date);

                var end_date = new Date(date);
                end_date.setDate(start_date.getDate() + 6);

                var week_format = 'd MMM yyyy, ddd';

                date_text = 'Week starting ' + kendo.toString(start_date, week_format, options.culture);
            } else if (date && options.select == 'month') {
                var month_format = 'MMM yyyy';
                date_text = kendo.toString(date, month_format, options.culture);
            }
            else if(date && options.select == 'quarter'){
                var quarter_ix = Math.floor(1+date.getMonth()/3);

                date_text = 'Q' + quarter_ix + ' ' + date.getFullYear();
            }  else if (date && options.select == 'year') {
                date_text = date.getFullYear();
            }

            that.element.val(date_text);

            that._updateARIA(date);

            return date;
        },

        getSelectedStartDate: function() {
            var that = this,
                options = that.options,
                date = this.value();

            if (date && options.select == 'week') {
                function getSunday(d) {
                    var sunday = new Date(d);
                    var day = d.getDay(),
                        diff = d.getDate() - day;

                    sunday.setDate(diff);
                    return sunday;
                }

                return getSunday(date);
            }
            else if (date && options.select == 'month') {
                return new Date(date.getFullYear(), date.getMonth(), 1);
            }
            else if (date && options.select == 'quarter') {
                return new Date(date.getFullYear(), 3*Math.floor(date.getMonth()/3), 1);
            }
            else if (date && options.select == 'year') {
                return new Date(date.getFullYear(), 0, 1);
            }

            return date;
        },

